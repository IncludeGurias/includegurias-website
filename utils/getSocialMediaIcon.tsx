const getSocialmediaIcon = ({ socialMedia, props }: { socialMedia: string; props?: IconBaseProps }) => {
  switch (socialMedia.toLocaleLowerCase().trim()) {
    case "instagram":
      return <FaInstagram {...props} />
    case "facebook":
      return <FaFacebook {...props} />
    case "youtube":
      return <FaYoutube {...props} />
    case "twitter":
      return <FaX {...props} />
    case "linkedin":
      return <FaLinkedin {...props} />
    case "email":
      return <FaEnvelope {...props} />
    case "github":
      return <FaGithub {...props} />
    case "meninas digitais":
      return <MeninasDigitaisIcon {...props} />
    case "chatbot":
      return <TbMessageChatbot {...props} />
    default:
      return <FaQuestion {...props} />
  }
}

